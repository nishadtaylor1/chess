/**
 * Build a Map from FEN key (first 4 parts) to opening info.
 * The deepest match (most moves) wins for each FEN.
 * @param {typeof ECO_BOOK} book
 * @param {Function} ChessClass - the Chess constructor from chess.js
 * @returns {Map}
 */
export function buildFenMap(ChessClass, book = ECO_BOOK) {
  const map = new Map()
  book.forEach((entry, entryIdx) => {
    const tmp = new ChessClass()
    for (let i = 0; i < entry.moves.length; i++) {
      if (!tmp.move(entry.moves[i])) break
      const key = tmp.fen().split(' ').slice(0, 4).join(' ')
      const existing = map.get(key)
      if (!existing || i + 1 > existing.depth) {
        map.set(key, {
          eco:         entry.eco,
          name:        entry.name,
          description: entry.description || '',
          depth:       i + 1,
          idx:         entryIdx,
        })
      }
    }
  })
  return map
}

